            <p>Arturo Pérez-Reverte, con un total de siete novelas convertidas en películas. La primera de ellas, “El maestro de esgrima”, fue llevada al cine por Pedro Olea en 1992,
                 con un reparto estelar que encabezaban Joaquim Almeida y Assumpta Serna. Su buena acogida permitió a Reverte dedicarse de lleno a la literatura con un gran éxito.
                  Famosas son las adaptaciones de “La tabla de Flandes” (1994), “Cachito” (1996) o las más recientes “Alatriste” (2006) -basada en la saga de aventuras del espadachín
