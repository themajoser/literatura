                        y familiares. ¿Qué pasa cuando convergen estos hombres y mujeres? Sobre eso habla la novela de Alicia Giménez Bartlett, ganadora del Premio Planeta en 2015 con esta historia que mezcla sexo, amistad, inocencia y maldad.
