                        “Una muerte inesperada. Un oscuro secreto familiar. La búsqueda de la verdad en el corazón de una tierra legendaria.” Esta es la premisa de Todo esto de daré, una novela en la que el lector atestiguará la investigación que lleva a cabo un hombre para descubrir la verdad detrás de la muerte de su marido. Una muerte en la cual
                         parece haber algún tipo de implicación por parte de la familia del fallecido. Con esta trama, la novela escrita por Dolores Redondo consiguió el Premio Planeta en 2016 y se ha convertido en uno de los libros más vendidos y aclamados.
