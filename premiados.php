                        la protagonista cree haber tenido una aventura con el mismo hombre que en el pasado. Es entonces cuando decidirá buscar más pistas sobre este amante y los sentimientos del mismo. Las respuestas podrían estar en la novela que dicho hombre tenía: Niebla en Tánger.
